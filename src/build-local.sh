#!/bin/bash

# ========================================
# Локальная сборка проекта ИЭО
# ========================================

echo ""
echo "╔═══════════════════════════════════════════════════╗"
echo "║   🚀 Локальная сборка проекта ИЭО                ║"
echo "╚═══════════════════════════════════════════════════╝"
echo ""

# Проверка наличия Node.js
if ! command -v node &> /dev/null; then
    echo "❌ Node.js не установлен!"
    echo "Установите Node.js: https://nodejs.org/"
    exit 1
fi

echo "✅ Node.js версия: $(node -v)"
echo "✅ npm версия: $(npm -v)"
echo ""

# Установка зависимостей
echo "📦 Установка зависимостей..."
npm install

if [ $? -ne 0 ]; then
    echo "❌ Ошибка при установке зависимостей!"
    exit 1
fi

echo "✅ Зависимости установлены"
echo ""

# Удаление старой сборки
if [ -d "build" ]; then
    echo "🗑️  Удаление старой сборки..."
    rm -rf build
fi

# Сборка проекта
echo "🔨 Сборка production версии..."
npm run build

if [ $? -ne 0 ]; then
    echo "❌ Ошибка при сборке проекта!"
    exit 1
fi

echo ""
echo "╔═══════════════════════════════════════════════════╗"
echo "║   ✅ Проект успешно собран!                      ║"
echo "╚═══════════════════════════════════════════════════╝"
echo ""

# Проверка результата
if [ -d "build" ]; then
    echo "📁 Содержимое папки build/:"
    ls -lh build/
    echo ""
    
    if [ -d "build/assets" ]; then
        echo "📁 Содержимое папки build/assets/:"
        ls -lh build/assets/
        echo ""
    fi
    
    echo "╔═══════════════════════════════════════════════════╗"
    echo "║   📤 Следующие шаги:                             ║"
    echo "╚═══════════════════════════════════════════════════╝"
    echo ""
    echo "ВАРИАНТ 1: Быстрая загрузка через SCP"
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
    echo "scp -r build/* root@217.114.15.80:/var/www/ivo/build/"
    echo ""
    echo "Затем установите права через SSH:"
    echo "ssh root@217.114.15.80"
    echo "chown -R www-data:www-data /var/www/ivo/build/"
    echo "chmod -R 755 /var/www/ivo/build/"
    echo "systemctl restart nginx"
    echo ""
    echo "ВАРИАНТ 2: Автоматическая загрузка"
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
    echo "./deploy-build.sh"
    echo ""
    echo "После загрузки откройте http://иэо.рф"
    echo "и нажмите Ctrl+Shift+R для очистки кеша"
    echo ""
    echo "╔═══════════════════════════════════════════════════╗"
    echo "║   🎉 Готово к загрузке!                          ║"
    echo "╚═══════════════════════════════════════════════════╝"
else
    echo "❌ Папка build/ не найдена!"
    echo "Проверьте логи сборки выше"
    exit 1
fi

echo ""
